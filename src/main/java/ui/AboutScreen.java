package ui;

import javax.swing.*;
import java.awt.*;

public class AboutScreen extends JPanel {

    public AboutScreen(AppController controller) {

        setLayout(new BorderLayout());

        String aboutText =
                "Το StateBudgetManager είναι μια εφαρμογή που επιτρέπει στον χρήστη να εξερευνήσει, " +
                "να κατανοήσει και να αλληλεπιδράσει με τον κρατικό προϋπολογισμό με απλό και οργανωμένο τρόπο. " +
                "Παρέχει πρόσβαση σε βασικές κατηγορίες εσόδων και δαπανών, ομαδοποιημένες πληροφορίες, " +
                "και δυνατότητα προβολής των συνολικών μεγεθών, ώστε ο χρήστης να έχει μια καθαρή και άμεση εικόνα " +
                "της δημοσιονομικής δομής.\n\n" +

                "Ο σκοπός της εφαρμογής είναι να δώσει στους νέους τη δυνατότητα να αναπτύξουν μια πιο ολοκληρωμένη, " +
                "δομημένη και συνειδητή οικονομική και πολιτική αντίληψη. Με άμεση πρόσβαση στον προϋπολογισμό, οι χρήστες " +
                "μπορούν να κατανοούν καλύτερα πώς καταγράφονται τα δημόσια έσοδα και πού κατευθύνονται οι δημόσιες δαπάνες, " +
                "ενισχύοντας έτσι την ενεργή συμμετοχή και τη γνώση γύρω από κρίσιμες οικονομικές αποφάσεις.\n\n" +

                "Κεντρικό στοιχείο της εφαρμογής αποτελεί η δυνατότητα δημιουργίας και επεξεργασίας σεναρίων αλλαγών, " +
                "επιτρέποντας στον χρήστη να εξετάζει τις επιπτώσεις πιθανών μεταβολών σε διάφορα δημοσιονομικά μεγέθη. " +
                "Επιπλέον, η εφαρμογή προσφέρει εργαλεία για παραγωγή αναφορών, προβολή δεδομένων με δομημένους πίνακες " +
                "και εύκολη κατηγοριοποίηση των πληροφοριών.\n\n" +

                "Με αυτόν τον τρόπο, το StateBudgetManager λειτουργεί ως εκπαιδευτικό και πρακτικό εργαλείο, ικανό να βοηθήσει " +
                "άτομα που επιθυμούν να κατανοήσουν βαθύτερα τον τρόπο λειτουργίας του κράτους μέσα από έναν από τους πιο " +
                "σημαντικούς θεσμούς του — τον προϋπολογισμό.";

        JTextArea textArea = new JTextArea(aboutText);
        textArea.setEditable(false);
        textArea.setLineWrap(true);
        textArea.setWrapStyleWord(true);

        JScrollPane scroll = new JScrollPane(textArea);

        JButton back = new JButton("Back");
        back.addActionListener(e -> controller.showScreen(AppController.LANDING));

        add(scroll, BorderLayout.CENTER);
        add(back, BorderLayout.SOUTH);
    }
}
